/*

Truthy:

In JavaScript, a truthy value is a value that is considered true when encountered in a Boolean context.

All values are truthy unless they are defined as falsy. 

That is, all values are truthy except false, 0, -0, 0n, "", null, undefined, and NaN.

Reference: https://developer.mozilla.org/en-US/docs/Glossary/Truthy


Falsy: 

A falsy (sometimes written falsey) value is a value that is considered false when encountered 
in a Boolean context.

Reference: https://developer.mozilla.org/en-US/docs/Glossary/Falsy

*/